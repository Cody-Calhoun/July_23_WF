// What is Javascript?
// Javascript is a programming language that adds interactivity to your website.
// It is used to create dynamic content for web pages.
// It is a scripting language that is executed by the browser.
// It is a lightweight, interpreted programming language.

// What is a script?
// A script is a series of instructions that a computer can follow to achieve a goal.
// You could compare scripts to any of the following:
// Recipes, handbooks, manuals, instruction guides, etc.


// Console.log() definition
// The console.log() method writes a message to the console.
// The console is useful for testing purposes.

// Example:
// console.log("Hello World!");

// What is a variable?
// A variable is a container for a value.
// You can think of variables as little containers for information that live in a computer's memory.
// Information stored in variables can be updated later on.

// Example:
// let myName = "John Doe";
// console.log(myName);

// What is a string?
// A string is a sequence of characters.
// A string can be any text inside double or single quotes.

// Example:
// var myName = "John Doe";
// console.log(myName);

// What is concatenation?
// Concatenation is the process of appending one string to the end of another string.
// You can concatenate strings using the + operator.

// Example:
// var firstName = "John";
// var lastName = "Doe";
// var fullName = `${firstName} ${lastName}`;
// console.log(fullName);

// What is a number?
// A number is a numeric data type in JavaScript.
// Numbers can be written with or without decimals.

// Example:
// var x = 5;
// var y = 5.5;
// console.log(x);
// console.log(y);

// What is an operator?
// An operator is a mathematical symbol that produces a result based on two values (or variables).

// Example:
// var x = 5;
// var y = 5;
// var z = x + y;
// console.log(z);

// What is the difference between the = and == operators?
// The = operator is used to assign a value to a variable.
// The == operator is used to compare two values.

// Example:
// var x = 5;
// var y = 5;
// var z = x == y;
// console.log(z);

// What is the difference between the == and === operators?
// The == operator compares two values and returns true if they are equivalent.
// The === operator compares two values and returns true only if they are equal and of the same type.

// Example:
// var x = 5;
// var y = "5";
// var z = x == y;
// console.log(z);

// var x = 5;
// var y = "5";
// var z = x === y;
// console.log(z);

// What is the difference between the != and !== operators?
// The != operator returns true if the operands are not equal.
// The !== operator returns true if the operands are not equal and/or not of the same type.

// Example:
// var x = 5;
// var y = "5";
// var z = x !== y;
// console.log(z);



// What is the difference between the > and >= operators?
// The > operator returns true if the left operand is greater than the right operand.
// The >= operator returns true if the left operand is greater than or equal to the right operand.

// Example:
// var x = 5;
// var y = 5;
// var z = x > y;

// For Loops
// For loops are used to execute a block of code a specified number of times.
// The for loop has the following syntax:
// for (statement 1; statement 2; statement 3) {
//     code block to be executed
// }

// Statement 1 is executed before the loop starts.
// Statement 2 defines the condition for running the loop (the loop will continue to run as long as this condition is true).
// Statement 3 is executed each time after the loop has been executed.

// Example:
// for (var i = 0; i < 10; i++) {
//     console.log(i);
// }

// Functions
// A function is a block of code designed to perform a particular task.
// A function is executed when something invokes it (calls it).
// Functions are defined with the function keyword, followed by a name, followed by parentheses ().
// Function names can contain letters, digits, underscores, and dollar signs (same rules as variables).
// The parentheses may include parameter names separated by commas: (parameter1, parameter2, ...).
// The code to be executed, by the function, is placed inside curly brackets: {}.

// Example:
function myFunction() {
    console.log("Hello World!");
}


// Function Parameters
// Function parameters are the names listed in the function definition.
// Function arguments are the real values passed to (and received by) the function.
// Function parameters are listed inside the parentheses () in the function definition.
// Function arguments are the values received by the function when it is invoked.
// Inside the function, the arguments (the parameters) behave as local variables.

// Example:
// function myFunction(x, y=5) {
//     // x = 7, y = 5
//     console.log(x + y);
// }

// myFunction(7);



